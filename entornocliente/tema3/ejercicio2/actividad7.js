let numero = 25;
// mostramos los resultados de los metodos Math mas utilizados en la consola
console.log("valor absoluto: ", Math.abs(numero));
console.log("redondeo hacia arriba: ", Math.ceil(numero));
console.log("redondeo hacia abajo: ", Math.floor(numero));
console.log("redondeo al entero mas cercano: ", Math.round(numero));
console.log("maximo entre 1 , 2 , 3: ", Math.max(1, 2, 3));
console.log("minimo entre 1 , 2 , 3: ", Math.min(1, 2, 3));
console.log(numero, " elevado a 3: ", Math.pow(numero, 3));
console.log("raiz cuadrada de ", numero, ": ", Math.sqrt(numero));
console.log("numero aleatorio entre 0 y 1: ", Math.random());
console.log("seno de", numero, ": ", Math.sin(numero));
console.log("coseno de", numero, ": ", Math.cos(numero));
console.log("tangente de", numero, ": ", Math.tan(numero));
console.log("logaritmo de", numero, ": ", Math.log(numero));
console.log("elevado a la potencia de ", numero, ": ", Math.exp(numero));
